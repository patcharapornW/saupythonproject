#เชื่อม/ต่อ/รวม ข้อมูลหลายยๆประเภทเข้าด้วยกัน
#ใช้ ,
score = 500
print('hey....',17,True,123.2520,13*4,"jra",score)
#ใช้ +
print('hii'+str(15)+str(False)+str(17/2)+'jraaa'+str(score))
#ใช้ เมธอด  format
print('hello {} {} {} {} jraa {}'.format(17,True,123.250,13*4.,score))
#ใช้ f-string 
print(f'hello {17} {True} {123.2520} {17/2} jraaa {score}')
#ใช้ modular operator %
print('hello.. %d %f hi...' % (20, 17.5))